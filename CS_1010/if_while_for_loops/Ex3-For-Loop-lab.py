#creates a list of integers
my_list = [4, 76, 2, 234, 9, 71]

#prints the elements of the list on one line
print("input list is:", end=" ")
for integers in my_list:
    print(integers,",",end=" ")
    
print()
#prints the sum of the integers in the list
print("sum =",sum(my_list))

#prints the average of the elements in the list
averagelist = sum(my_list)/len(my_list)
print("average = ",averagelist)

#prints the largest integer of the list
count = 0
LargestInteger = 0
while count < len(my_list):
    if my_list[count] > LargestInteger:
        LargestInteger = my_list[count]
    count+=1
    
print("largest number =", LargestInteger)
print("------------")

