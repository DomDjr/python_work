#creates a list with strings
my_list = ['earth', 'exciting', 'day', 'laptop', 'electrochemical', 'blue', 'up']
LongestStringLength = 0
LongestString = 0
#prints the elements in the list as well as their corresponding lengths. Prints the largest string in the list
for items in my_list:
    print(items, end=" ")
    print(len(items))
    if len(items) > LongestStringLength:
        LongestStringLength = len(items)
        LongestString = items
           
  
print("The longest word is", LongestString)
print()
print('-----------')

